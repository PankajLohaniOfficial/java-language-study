public class d_increment_and_decrement_operator {
    public static void main ( String [] args){
        int i = 56;
        System.out.println(i++); // The output of this will be - > 56 since the value is first used then it is incremented..
        System.out.println(++i); // The output of this will be - > 58 since in previously the 56 was increased to 57 and then now it is first updated then it is use ....
    }
}
